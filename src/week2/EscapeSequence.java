package week2;

public class EscapeSequence {

    public static void main(String[] args) {
        System.out.println("Java\nSDET");

        System.out.println("Java\tSDET");

        //- print "Hello Cydeo "Batch 40" Friends!" using "" double quotes
        System.out.println("Hello Cydeo \"Batch 40\" Friends!");

        //- print "Let's code "java" everyday!"
        System.out.println("Let\'s code \"java\" everyday!");

        //- print "/Users/cydeo/IdeaProjects/java-live-review"
        System.out.println("/Users/cydeo/IdeaProjects/java-live-review");

        //- print "C:\Users\cydeo\IdeaProjects\java-live-review"
        System.out.println("C:\\Users\\cydeo\\IdeaProjects\\java-live-review");
    }
}
