package week2.variables;

public class BooleanType {

    public static void main(String[] args) {
        //You are looking at Amazon products for sales with free shipping:
        boolean freeShipping = true;
        boolean onSale = false;
        System.out.println(freeShipping);
        System.out.println(onSale);

        //Mike did not come to class today:
        boolean isMikePresent = false;
        System.out.println("Is student present? " + isMikePresent);

        //You are checking if your package is delivered, it is yes.
        boolean isDelivered = true;
        System.out.println("My package has been delivered? " + isDelivered);

        //You are at the traffic light and checking if green light, it is still red:
        boolean isGreenLight = false;
        System.out.println("Can I go? - " + isGreenLight);


    }
}
